# Import Math module
from math import *

#Taking a number input from user
no = int(input('Enter a number: '))

#Performing operations on the number
op1 = sqrt(float(no))
op2 = log(no)
op3 = sin(no)

#Printing the outcomes
print('Square root:', op1)
print('Logarithm:', op2)
print('Sine:', op3)